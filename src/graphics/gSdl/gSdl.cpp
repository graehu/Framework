int gSdl::loadImage(char* _fileName)
{

  SDL_Surface* temp;
  temp = SDL_LoadBMP(_fileName);

  if (temp == NULL)
    {
      printf("Unable to load bitmap: %s\n", SDL_GetError());
      return 1;
    }
  else m_images.push_back(*temp);

  return m_images.size() - 1;
}




